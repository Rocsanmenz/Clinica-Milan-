    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        CRUD_CitasMedicas cm = new CRUD_CitasMedicas();
        try {
            if ((jComboIDCliente.getText().equals(""))
                    || (jComboIDDoctor.getText().equals(""))
                    || (jTextFecha.getText().equals(""))
                    || (jTextHora.getText().equals(""))) {
                JOptionPane.showMessageDialog(null, "Tiene datos vacÃ­os");
            } else {
                if (cm.verificarDatos(jComboIDCliente.getText())) {
                    JOptionPane.showMessageDialog(null, "Ya existe cliente con ese nÃºmero de CÃ©dula");
                } else {
                    GuardarCitas();
                    limpiar();
                    JOptionPane.showMessageDialog(null, "Datos Guardados Correctamente");
                }

            }
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(null, "Error: " + e);
        }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

    private void jButtonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonActualizarActionPerformed
     try {
            if ((jTextIDCliente.getText().equals(""))
                    || (jTextIDDoctor.getText().equals(""))
                    || (jTextFecha.getText().equals(""))
                    || (jTextHora.getText().equals(""))) {
                JOptionPane.showMessageDialog(null, "Tiene datos vacÃ­os");
            } else {
                EditarCitas();
                JOptionPane.showMessageDialog(null, "Datos Actualizados Correctamente");
                dispose();

                JInternalFrameCitasAgendadas.botonmostrar.doClick();

            }

        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(null, "Error: " + e);
        }
    }//GEN-LAST:event_jButtonActualizarActionPerformed
   
